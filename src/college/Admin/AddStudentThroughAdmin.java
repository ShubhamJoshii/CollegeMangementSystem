    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            String hashedPassword = BCrypt.hashpw(defaultPassword.getText(), BCrypt.gensalt(12));
            Connection conn = DBConnection.getConnection();

            String sql = "INSERT INTO users (username, email, password, role) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql, Statement.RETURN_GENERATED_KEYS);

            pst.setString(1, fullName.getText());     // username
            pst.setString(2, email.getText());        // email
            pst.setString(3, hashedPassword);         // hashed password
            pst.setString(4, "Student");              // role

            int status = pst.executeUpdate();

            if (status > 0) {

                // Retrieve generated key
                ResultSet rs = pst.getGeneratedKeys();
                if (rs.next()) {
                    long studentId = rs.getLong(1);
                    String courseId = selectCourse.getSelectedItem().toString().split("\\|")[0].trim();
                    System.out.println("Inserted ID: " + studentId);
                    sql = "INSERT INTO students (studentId, courseId) VALUES (?, ?)";
                    pst = conn.prepareStatement(sql);
                    pst.setLong(1, studentId);
                    pst.setString(2, courseId);

                    status = pst.executeUpdate();
                    if (status > 0) {
                        JOptionPane.showMessageDialog(this,
                                "New Student Added Successfully!",
                                "Success",
                                JOptionPane.INFORMATION_MESSAGE
                        );
                        this.dispose();   
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this,
                        "Failed to add student!",
                        "Error",
                        JOptionPane.ERROR_MESSAGE
                );
            }

            pst.close();
            conn.close();

        } catch (ClassNotFoundException | SQLException e) {
            System.out.println("Error in register: " + e.getMessage());
            JOptionPane.showMessageDialog(this,
                    "Registration Unsuccessful: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }

    }//GEN-LAST:event_saveBtnActionPerformed

